class Multiply:
    def __init__(x, y):
        self.x = x
        self.y = y
    
    def mul(self):
        return self.x * self.y

class Add:
    def __init__(self, x, y):
        self.x = x
        self.y = y

    def cal(self):
        return self.x+self.y

class Divide:
    def __init__(self, x, y):
        self.x = x
        self.y = y

    def cal(self):
        return self.x / self.y
        
